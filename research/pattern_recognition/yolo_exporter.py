"""
YOLO format annotation exporter for pattern recognition.

Exports pattern detections in YOLO format for training object detection models.

YOLO Annotation Format:
    <class_id> <x_center> <y_center> <width> <height>
    - All values normalized to 0-1
    - One line per object in the image

Directory Structure for YOLO training:
    dataset/
        images/
            train/
            val/
        labels/
            train/
            val/
        data.yaml
"""

from pathlib import Path
from typing import List, Dict, Any, Optional
import logging
import random

from .models import AscendingTrianglePattern, BoundingBox, PATTERN_CLASSES

logger = logging.getLogger(__name__)


def export_yolo_annotation(
    pattern: AscendingTrianglePattern,
    save_path: str
) -> str:
    """
    Export single pattern annotation in YOLO format.

    Creates a .txt file with bounding box coordinates.

    Args:
        pattern: Pattern with bounding_box populated
        save_path: Path for .txt file (should match image filename)

    Returns:
        Path to saved annotation file

    Raises:
        ValueError: If pattern has no bounding_box
    """
    if pattern.bounding_box is None:
        raise ValueError("Pattern must have bounding_box calculated. "
                        "Call chart_generator.generate_yolo_training_image() first.")

    bbox = pattern.bounding_box

    # YOLO format: class_id x_center y_center width height
    line = bbox.to_yolo_string()

    Path(save_path).parent.mkdir(parents=True, exist_ok=True)
    with open(save_path, 'w') as f:
        f.write(line + '\n')

    logger.debug(f"Saved YOLO annotation to {save_path}")
    return save_path


def export_multiple_annotations(
    patterns: List[AscendingTrianglePattern],
    output_dir: str,
    filename_prefix: str = "pattern"
) -> List[str]:
    """
    Export multiple pattern annotations.

    Args:
        patterns: List of patterns with bounding boxes
        output_dir: Directory to save annotations
        filename_prefix: Prefix for annotation files

    Returns:
        List of saved file paths
    """
    saved_paths = []
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    for i, pattern in enumerate(patterns):
        if pattern.bounding_box is None:
            logger.warning(f"Skipping pattern {i}: no bounding box")
            continue

        filename = f"{filename_prefix}_{pattern.ticker}_{i:04d}.txt"
        save_path = output_path / filename

        export_yolo_annotation(pattern, str(save_path))
        saved_paths.append(str(save_path))

    logger.info(f"Exported {len(saved_paths)} annotations to {output_dir}")
    return saved_paths


def create_yolo_dataset_structure(
    base_dir: str,
    create_val: bool = True
) -> Dict[str, Path]:
    """
    Create YOLO dataset directory structure.

    Creates:
        base_dir/
            images/
                train/
                val/    (if create_val=True)
            labels/
                train/
                val/    (if create_val=True)

    Args:
        base_dir: Base directory for dataset
        create_val: Whether to create validation directories

    Returns:
        Dict mapping directory names to Path objects
    """
    base_path = Path(base_dir)
    dirs = {}

    # Create directories
    dirs['images_train'] = base_path / "images" / "train"
    dirs['labels_train'] = base_path / "labels" / "train"

    if create_val:
        dirs['images_val'] = base_path / "images" / "val"
        dirs['labels_val'] = base_path / "labels" / "val"

    for name, path in dirs.items():
        path.mkdir(parents=True, exist_ok=True)
        logger.debug(f"Created directory: {path}")

    logger.info(f"Created YOLO dataset structure at {base_dir}")
    return dirs


def generate_data_yaml(
    output_dir: str,
    class_names: Optional[List[str]] = None
) -> str:
    """
    Generate YOLO data.yaml configuration file.

    Args:
        output_dir: Dataset base directory
        class_names: List of class names (default: uses PATTERN_CLASSES)

    Returns:
        Path to generated data.yaml
    """
    if class_names is None:
        # Sort by class ID to ensure consistent ordering
        sorted_classes = sorted(PATTERN_CLASSES.items(), key=lambda x: x[1])
        class_names = [name for name, _ in sorted_classes]

    output_path = Path(output_dir)
    yaml_path = output_path / "data.yaml"

    # Build YAML content
    yaml_lines = [
        f"# YOLO Dataset Configuration",
        f"# Generated by pattern_recognition module",
        f"",
        f"path: {output_path.absolute()}",
        f"train: images/train",
        f"val: images/val",
        f"",
        f"# Number of classes",
        f"nc: {len(class_names)}",
        f"",
        f"# Class names",
        f"names:",
    ]

    for i, name in enumerate(class_names):
        yaml_lines.append(f"  {i}: {name}")

    yaml_content = "\n".join(yaml_lines)

    with open(yaml_path, 'w') as f:
        f.write(yaml_content)

    logger.info(f"Generated data.yaml at {yaml_path}")
    return str(yaml_path)


def export_dataset(
    patterns: List[AscendingTrianglePattern],
    image_paths: List[str],
    output_dir: str,
    split_ratio: float = 0.8,
    shuffle: bool = True
) -> Dict[str, Any]:
    """
    Export complete YOLO dataset from patterns.

    Organizes images and annotations into train/val splits.

    Args:
        patterns: List of patterns with bounding boxes
        image_paths: Corresponding image paths for each pattern
        output_dir: Base output directory
        split_ratio: Train/val split ratio (default 0.8 = 80% train)
        shuffle: Whether to shuffle before splitting

    Returns:
        Statistics dict with counts
    """
    if len(patterns) != len(image_paths):
        raise ValueError(f"Pattern count ({len(patterns)}) must match "
                        f"image count ({len(image_paths)})")

    # Filter patterns with bounding boxes
    valid_items = [
        (p, img) for p, img in zip(patterns, image_paths)
        if p.bounding_box is not None
    ]

    if not valid_items:
        logger.warning("No patterns with bounding boxes to export")
        return {"train": 0, "val": 0}

    # Shuffle if requested
    if shuffle:
        random.shuffle(valid_items)

    # Create directory structure
    dirs = create_yolo_dataset_structure(output_dir)

    # Split into train/val
    n_train = int(len(valid_items) * split_ratio)
    train_items = valid_items[:n_train]
    val_items = valid_items[n_train:]

    stats = {"train": 0, "val": 0}

    # Process training items
    for pattern, img_path in train_items:
        img_name = Path(img_path).stem
        label_path = dirs['labels_train'] / f"{img_name}.txt"
        export_yolo_annotation(pattern, str(label_path))
        stats["train"] += 1

    # Process validation items
    for pattern, img_path in val_items:
        img_name = Path(img_path).stem
        label_path = dirs['labels_val'] / f"{img_name}.txt"
        export_yolo_annotation(pattern, str(label_path))
        stats["val"] += 1

    # Generate data.yaml
    generate_data_yaml(output_dir)

    logger.info(f"Exported YOLO dataset: train={stats['train']}, val={stats['val']}")
    return stats


def validate_annotation(annotation_path: str) -> Dict[str, Any]:
    """
    Validate a YOLO annotation file.

    Checks:
    - File exists and is readable
    - Correct format (5 values per line)
    - Values in valid range (0-1 for coordinates)

    Args:
        annotation_path: Path to .txt annotation file

    Returns:
        Dict with validation results
    """
    result = {
        "valid": True,
        "errors": [],
        "num_objects": 0
    }

    try:
        with open(annotation_path, 'r') as f:
            lines = f.readlines()

        for i, line in enumerate(lines):
            line = line.strip()
            if not line:
                continue

            parts = line.split()
            if len(parts) != 5:
                result["valid"] = False
                result["errors"].append(f"Line {i+1}: Expected 5 values, got {len(parts)}")
                continue

            try:
                class_id = int(parts[0])
                x, y, w, h = float(parts[1]), float(parts[2]), float(parts[3]), float(parts[4])

                # Validate ranges
                if class_id < 0:
                    result["errors"].append(f"Line {i+1}: Negative class_id")
                    result["valid"] = False

                for name, val in [('x', x), ('y', y), ('w', w), ('h', h)]:
                    if not (0 <= val <= 1):
                        result["errors"].append(f"Line {i+1}: {name}={val} out of range [0,1]")
                        result["valid"] = False

                result["num_objects"] += 1

            except ValueError as e:
                result["valid"] = False
                result["errors"].append(f"Line {i+1}: Parse error - {e}")

    except FileNotFoundError:
        result["valid"] = False
        result["errors"].append("File not found")
    except Exception as e:
        result["valid"] = False
        result["errors"].append(f"Read error: {e}")

    return result


def get_dataset_stats(dataset_dir: str) -> Dict[str, Any]:
    """
    Get statistics about a YOLO dataset.

    Args:
        dataset_dir: Base dataset directory

    Returns:
        Dict with dataset statistics
    """
    base_path = Path(dataset_dir)

    stats = {
        "train_images": 0,
        "train_labels": 0,
        "val_images": 0,
        "val_labels": 0,
        "total_objects": 0,
        "class_distribution": {}
    }

    # Count files
    train_images = base_path / "images" / "train"
    train_labels = base_path / "labels" / "train"
    val_images = base_path / "images" / "val"
    val_labels = base_path / "labels" / "val"

    if train_images.exists():
        stats["train_images"] = len(list(train_images.glob("*.png")))
    if train_labels.exists():
        stats["train_labels"] = len(list(train_labels.glob("*.txt")))
    if val_images.exists():
        stats["val_images"] = len(list(val_images.glob("*.png")))
    if val_labels.exists():
        stats["val_labels"] = len(list(val_labels.glob("*.txt")))

    # Count objects and class distribution
    for labels_dir in [train_labels, val_labels]:
        if not labels_dir.exists():
            continue

        for label_file in labels_dir.glob("*.txt"):
            with open(label_file, 'r') as f:
                for line in f:
                    line = line.strip()
                    if line:
                        parts = line.split()
                        if len(parts) >= 1:
                            class_id = parts[0]
                            stats["total_objects"] += 1
                            stats["class_distribution"][class_id] = \
                                stats["class_distribution"].get(class_id, 0) + 1

    return stats
