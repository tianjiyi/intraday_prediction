"""
Generate YOLO Training Data for W_Bottom Pullback Detection

Scans existing backtest results (yolo_backtest_output/*.json) and generates
training images for two classes:
  - Class 0: w_bottom_breakout (at detection time)
  - Class 1: w_bottom_pullback_support (at limit fill time)

Only uses trades where limit_filled=True (pullback actually occurred).
"""

import json
import os
import sys
from pathlib import Path
from datetime import datetime, timedelta
import pandas as pd
import numpy as np
import mplfinance as mpf
import matplotlib.pyplot as plt
import pytz

# Add parent directory for imports
sys.path.insert(0, str(Path(__file__).parent.parent.parent))

from pattern_recognition.data_fetcher import fetch_single_ticker


# Configuration
BACKTEST_OUTPUT_DIR = Path(__file__).parent.parent / "yolo_backtest_output_4yr"
DATASET_OUTPUT_DIR = Path(__file__).parent.parent / "yolo_dataset_stages"
CHART_WINDOW = 150  # Bars to show in chart
IMAGE_SIZE = 640    # YOLO training image size


def setup_directories():
    """Create dataset directory structure."""
    dirs = [
        DATASET_OUTPUT_DIR / "images" / "train",
        DATASET_OUTPUT_DIR / "images" / "val",
        DATASET_OUTPUT_DIR / "labels" / "train",
        DATASET_OUTPUT_DIR / "labels" / "val",
    ]
    for d in dirs:
        d.mkdir(parents=True, exist_ok=True)
    print(f"Created dataset directories at: {DATASET_OUTPUT_DIR}")


def create_data_yaml():
    """Create YOLO data.yaml config file."""
    # Use absolute path to avoid YOLO path resolution issues
    abs_path = str(DATASET_OUTPUT_DIR.resolve()).replace('\\', '/')
    yaml_content = f"""# YOLO Dataset for W_Bottom Stage Detection
# Auto-generated by generate_pullback_training_data.py

path: {abs_path}
train: images/train
val: images/val

nc: 2

names:
  0: w_bottom_breakout
  1: w_bottom_pullback_support
"""
    yaml_path = DATASET_OUTPUT_DIR / "data.yaml"
    yaml_path.write_text(yaml_content)
    print(f"Created data.yaml at: {yaml_path}")


def find_profitable_trades(year_filter: str = None):
    """Scan backtest output and find all profitable trades.

    Args:
        year_filter: Optional year to filter (e.g., "2025"). If None, process all years.

    Returns list of trades with 'has_pullback' flag indicating if pullback occurred.
    """
    trades = []

    if year_filter:
        json_files = list(BACKTEST_OUTPUT_DIR.glob(f"*{year_filter}*_signals.json"))
        print(f"Found {len(json_files)} signal files for {year_filter}")
    else:
        json_files = list(BACKTEST_OUTPUT_DIR.glob("*_signals.json"))
        print(f"Found {len(json_files)} signal files")

    for json_file in json_files:
        try:
            with open(json_file, 'r') as f:
                data = json.load(f)

            date = data.get('date', '')
            signals = data.get('signals', [])

            for sig in signals:
                # Only include profitable trades
                if sig.get('trade_outcome') != 'profit':
                    continue

                # Check if pullback occurred
                has_pullback = False

                # New format: explicit limit_filled field
                if sig.get('limit_filled', False):
                    has_pullback = True
                # Old format: MAE < 0 means price dipped after entry (pullback)
                elif sig.get('mae', 0) < 0:
                    has_pullback = True

                trades.append({
                    'file': json_file,
                    'date': date,
                    'signal': sig,
                    'ticker': json_file.stem.split('_')[0],
                    'has_pullback': has_pullback
                })
        except Exception as e:
            print(f"Error reading {json_file}: {e}")

    pullback_count = sum(1 for t in trades if t['has_pullback'])
    print(f"Found {len(trades)} profitable trades ({pullback_count} with pullback)")
    return trades


def fetch_day_data(ticker: str, date_str: str):
    """Fetch intraday data for a specific date."""
    eastern = pytz.timezone('US/Eastern')
    target_date = datetime.strptime(date_str, '%Y-%m-%d').date()

    # Calculate lookback needed
    today = datetime.now(eastern).date()
    days_back = (today - target_date).days + 5  # Buffer

    # Fetch data
    df = fetch_single_ticker(
        ticker=ticker,
        lookback_bars=max(500, days_back * 390),  # ~390 bars per day
        timeframe='1min',
        rth_only=True
    )

    if df is None or df.empty:
        return None

    # Filter to target date
    df['timestamp'] = pd.to_datetime(df['timestamp'])
    df['date'] = df['timestamp'].dt.date
    day_df = df[df['date'] == target_date].reset_index(drop=True)

    return day_df


def generate_chart_image(df: pd.DataFrame, output_path: Path, title: str = ""):
    """Generate a 640x640 candlestick chart image for YOLO training."""
    if df.empty or len(df) < 10:
        return False

    # Prepare data for mplfinance
    chart_df = df.copy()
    chart_df.set_index('timestamp', inplace=True)
    chart_df = chart_df.rename(columns={
        'open': 'Open',
        'high': 'High',
        'low': 'Low',
        'close': 'Close',
        'volume': 'Volume'
    })

    # Custom style for clean chart
    mc = mpf.make_marketcolors(
        up='green', down='red',
        edge='inherit',
        wick='inherit',
        volume='gray'
    )
    style = mpf.make_mpf_style(
        marketcolors=mc,
        gridstyle='',
        facecolor='white',
        edgecolor='white'
    )

    # Create figure
    fig, axes = mpf.plot(
        chart_df,
        type='candle',
        style=style,
        volume=False,  # No volume for cleaner pattern
        returnfig=True,
        figsize=(6.4, 6.4),  # Will be resized to 640x640
        tight_layout=True
    )

    # Remove all axes decorations for clean training image
    for ax in axes:
        ax.set_xlabel('')
        ax.set_ylabel('')
        ax.set_xticklabels([])
        ax.set_yticklabels([])
        ax.tick_params(left=False, bottom=False)
        for spine in ax.spines.values():
            spine.set_visible(False)

    # Save
    fig.savefig(
        str(output_path),
        dpi=100,
        bbox_inches='tight',
        pad_inches=0,
        facecolor='white'
    )
    plt.close(fig)

    return True


def calculate_bbox_from_signal(signal: dict, df: pd.DataFrame, window_bars: int):
    """
    Calculate YOLO bounding box (normalized 0-1) from signal data.

    The bbox should cover the W_Bottom pattern area.
    Uses the actual price data in the chart to find where the pattern is.
    """
    # Get pattern info from signal
    range_high = signal.get('range_high', 0)
    range_low = signal.get('range_low', 0)
    actual_pattern_low = signal.get('actual_pattern_low', range_low)
    pattern_bars = signal.get('pattern_bars', 50)

    # Price range of chart (for y normalization)
    price_min = df['low'].min()
    price_max = df['high'].max()
    price_range = price_max - price_min

    if price_range == 0:
        price_range = 1  # Avoid division by zero

    # X: Pattern is in the last portion of chart (ending at right edge)
    # W-Bottom pattern typically spans 30-50 bars, use fixed reasonable width
    width = 0.35  # Fixed 35% width - covers pattern without being too large
    x_right = 0.95
    x_left = x_right - width
    x_center = (x_left + x_right) / 2

    # Y: Use signal's range_high and actual_pattern_low for precise positioning
    # These are the actual pattern boundaries from the detector
    if range_high > 0 and actual_pattern_low > 0:
        # Use signal prices if within chart range
        if actual_pattern_low >= price_min and range_high <= price_max:
            pattern_high = range_high
            pattern_low = actual_pattern_low
        else:
            # Fallback to last portion of chart
            pattern_df = df.tail(min(60, len(df)))
            pattern_low = pattern_df['low'].min()
            pattern_high = pattern_df['high'].max()
    else:
        # Fallback to last portion of chart
        pattern_df = df.tail(min(60, len(df)))
        pattern_low = pattern_df['low'].min()
        pattern_high = pattern_df['high'].max()

    # Add small buffer (10%)
    buffer = (pattern_high - pattern_low) * 0.10
    y_top_price = pattern_high + buffer
    y_bottom_price = pattern_low - buffer

    # Clamp to chart range
    y_top_price = max(price_min, min(price_max, y_top_price))
    y_bottom_price = max(price_min, min(price_max, y_bottom_price))

    # Normalize and invert (image y=0 is top)
    y_top = 1 - ((y_top_price - price_min) / price_range)
    y_bottom = 1 - ((y_bottom_price - price_min) / price_range)

    # Ensure y_top < y_bottom (in image coords, top is smaller)
    if y_top > y_bottom:
        y_top, y_bottom = y_bottom, y_top

    y_center = (y_top + y_bottom) / 2
    height = abs(y_bottom - y_top)

    # Constrain to reasonable size (25-50% of image)
    height = max(0.25, min(0.50, height))

    return x_center, y_center, width, height


def generate_training_pair(trade: dict, idx: int, split: str = 'train'):
    """
    Generate breakout and pullback training images for a single trade.

    Args:
        trade: Trade data dict
        idx: Index for file naming
        split: 'train' or 'val'

    Returns:
        Tuple of (breakout_generated, pullback_generated)
    """
    signal = trade['signal']
    date_str = trade['date']
    ticker = trade['ticker']

    # Parse timestamps
    detection_time_str = signal.get('detection_time', '')

    try:
        # Handle timezone-aware timestamp
        if '-04:00' in detection_time_str or '-05:00' in detection_time_str:
            detection_time = datetime.fromisoformat(detection_time_str)
        else:
            detection_time = datetime.strptime(detection_time_str, '%Y-%m-%d %H:%M:%S')
    except Exception as e:
        print(f"  Error parsing detection_time: {e}")
        return False, False

    # Fetch day data
    print(f"  Fetching data for {ticker} {date_str}...")
    day_df = fetch_day_data(ticker, date_str)

    if day_df is None or day_df.empty:
        print(f"  No data for {date_str}")
        return False, False

    # Make timestamp timezone-naive for comparison
    day_df['timestamp_naive'] = day_df['timestamp'].dt.tz_localize(None)
    detection_time_naive = detection_time.replace(tzinfo=None)

    # Find detection bar index
    detection_mask = day_df['timestamp_naive'] <= detection_time_naive
    if not detection_mask.any():
        print(f"  Detection time not found in data")
        return False, False

    detection_idx = detection_mask.sum() - 1

    # Get pullback bar - handle both formats
    limit_fill_bar = signal.get('limit_fill_bar')
    entry_bar = signal.get('entry_bar', detection_idx + 1)
    has_pullback = trade.get('has_pullback', False)

    # For old format without limit_fill_bar, estimate pullback bar
    # Use entry_bar + 10 as approximation (pullback typically happens within 10 bars)
    if limit_fill_bar is None and has_pullback:
        limit_fill_bar = min(entry_bar + 10, len(day_df) - 1)

    breakout_generated = False
    pullback_generated = False

    # ===== BREAKOUT IMAGE (Class 0) =====
    # Chart ending at detection time
    start_idx = max(0, detection_idx - CHART_WINDOW + 1)
    breakout_df = day_df.iloc[start_idx:detection_idx + 1].copy()

    if len(breakout_df) >= 30:
        breakout_img_path = DATASET_OUTPUT_DIR / "images" / split / f"{ticker}_{date_str}_breakout_{idx:04d}.png"
        breakout_label_path = DATASET_OUTPUT_DIR / "labels" / split / f"{ticker}_{date_str}_breakout_{idx:04d}.txt"

        if generate_chart_image(breakout_df, breakout_img_path):
            # Calculate and write bbox
            x, y, w, h = calculate_bbox_from_signal(signal, breakout_df, len(breakout_df))
            breakout_label_path.write_text(f"0 {x:.6f} {y:.6f} {w:.6f} {h:.6f}")
            breakout_generated = True
            print(f"  Generated breakout image: {breakout_img_path.name}")

    # ===== PULLBACK IMAGE (Class 1) =====
    # Chart ending at limit fill bar (pullback moment)
    if limit_fill_bar is not None and limit_fill_bar < len(day_df):
        start_idx = max(0, limit_fill_bar - CHART_WINDOW + 1)
        pullback_df = day_df.iloc[start_idx:limit_fill_bar + 1].copy()

        if len(pullback_df) >= 30:
            pullback_img_path = DATASET_OUTPUT_DIR / "images" / split / f"{ticker}_{date_str}_pullback_{idx:04d}.png"
            pullback_label_path = DATASET_OUTPUT_DIR / "labels" / split / f"{ticker}_{date_str}_pullback_{idx:04d}.txt"

            if generate_chart_image(pullback_df, pullback_img_path):
                # Calculate and write bbox
                x, y, w, h = calculate_bbox_from_signal(signal, pullback_df, len(pullback_df))
                pullback_label_path.write_text(f"1 {x:.6f} {y:.6f} {w:.6f} {h:.6f}")
                pullback_generated = True
                print(f"  Generated pullback image: {pullback_img_path.name}")

    return breakout_generated, pullback_generated


def main():
    """Main entry point."""
    import argparse
    parser = argparse.ArgumentParser()
    parser.add_argument('--year', type=str, default=None, help='Filter by year (e.g., 2025)')
    args = parser.parse_args()

    print("="*60)
    print("W_Bottom Pullback Training Data Generator")
    if args.year:
        print(f"Filtering to year: {args.year}")
    print("="*60)

    # Setup
    setup_directories()
    create_data_yaml()

    # Find profitable trades
    trades = find_profitable_trades(year_filter=args.year)

    if not trades:
        print("No profitable trades found!")
        return

    # Generate training data
    breakout_count = 0
    pullback_count = 0
    val_ratio = 0.2  # 20% for validation

    for idx, trade in enumerate(trades):
        print(f"\nProcessing trade {idx+1}/{len(trades)}: {trade['ticker']} {trade['date']}")

        # Assign to train or val split
        split = 'val' if idx < len(trades) * val_ratio else 'train'

        try:
            b_gen, p_gen = generate_training_pair(trade, idx, split)
            if b_gen:
                breakout_count += 1
            if p_gen:
                pullback_count += 1
        except Exception as e:
            print(f"  Error: {e}")
            continue

    # Summary
    print("\n" + "="*60)
    print("GENERATION COMPLETE")
    print("="*60)
    print(f"Breakout images (class 0): {breakout_count}")
    print(f"Pullback images (class 1): {pullback_count}")
    print(f"Total images: {breakout_count + pullback_count}")
    print(f"Output directory: {DATASET_OUTPUT_DIR}")


if __name__ == "__main__":
    main()
